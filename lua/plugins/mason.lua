return {
  "williamboman/mason.nvim",
  opts = function(_, opts)
    table.insert(opts.ensure_installed, "clang_format")
    table.insert(opts.ensure_installed, "google-java-format")

    -- refresh the mason-registry in case of accessing packages
    -- that aren't available due to the registry not being downloaded yet
    local mason_registry = require("mason-registry")
    mason_registry.refresh(function()
      mason_registry.get_all_packages()
    end)
  end,
}
